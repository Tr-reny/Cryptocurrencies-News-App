package com.tr_reny.cryptonews.Model;

public class News {

    public String title;
    public String link;
    public String pubDate;
    public String source;
    public String guid;


    //Getters
    
    public String getTitle() {
        return title;
    }

    public String getLink() {
        return link;
    }

    public String getPubDate() {
        return pubDate;
    }

    public String getSource() {
        return source;
    }

    public String getGuid() {
        return guid;
    }
}
